CREATE TABLE PEDIDOS_ENVIOS(
   ID_ENVIO INT COMMENT 'PRIMARY KEY DE ENVIOS',
   ID_PEDIDO INT COMMENT 'PRIMARY KEY DE PEDIDOS',
