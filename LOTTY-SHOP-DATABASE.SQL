   ID_ARTICULO VARCHAR(6) COMMENT 'IDENTIFICADOR DEL ARTICULO',
   ID_LOTE VARCHAR(6) COMMENT 'IDENTIFICADOR DEL LOTE',
   CANTIDAD INT COMMENT 'CANTIDAD DE PRODUCTOS DE LA LINEA',
   PRECIO_UNITARIO DECIMAL (10,2) NOT NULL COMMENT 'PRECIO DE UN PRODUCTO UNITARIO',
   ESTADO ENUM('ROTURA','CADUCIDAD','TIEMPO DE PRUEBA'),
